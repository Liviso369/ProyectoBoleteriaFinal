    private void armarCabecera() {
        List<Object> columnas = new ArrayList<>();

        columnas.add("Ubicacion");
        columnas.add("Localidad");
        columnas.add("Estado");

        for (Object x : columnas) {

            modelo.addColumn(x);
        }

        jtSala.setModel(modelo);
    }

    private void personalizarTabla() {

        armarCabecera();
        th = jtSala.getTableHeader();
        Font fuente = new Font("Times New Roman", 0, 20);
        th.setFont(fuente);
        th.setBackground(new Color(249, 193, 205));
        jtSala.setFont(new Font("Times New Roman", 0, 20));
        jtSala.setRowHeight(30);
        jtSala.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }

    private void borrarFilas() {

        int a = modelo.getRowCount() - 1;

        for (int i = a; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }

    private void limpiarCampos() {
        jcbLocalidad.setSelectedIndex(0);
        jcbPelicula.setSelectedIndex(0);
        borrarFilas();
    }

    private void cargarComboLocalidad() {
        List<String> lista = new ArrayList<>();

        lista.add("San Luis");
        lista.add("Villa Mercedes");

        for (String x : lista) {

            jcbLocalidad.addItem(x);

        }
    }

    private void cargarComboPelicula() {
        for (Pelicula x : pd.obtenerPeliculas()) {

            jcbPelicula.addItem(x);
        }
    }

    private void traerXLocalidad() {
        String localidad = (String) jcbLocalidad.getSelectedItem();

        if (sd.obtenerSalasXLocalidad(localidad).isEmpty()) {

            JOptionPane.showMessageDialog(this, "No hay salas en esa localidad");

        } else {

            for (Sala x : sd.obtenerSalasXLocalidad(localidad)) {

                modelo.addRow(new Object[]{x.getUbicacion(), x.getLocalidad(), x.isEstado()});
            }

        }
    }

    private void traerXPelicula() {
        boolean validar = false;

        Pelicula pelicula = (Pelicula) jcbPelicula.getSelectedItem();

        Proyeccion proyeccion = new Proyeccion();

        if (prod.obtenerIdSalaXPelicula(pelicula.getIdPelicula()).isEmpty()) {

            JOptionPane.showMessageDialog(this, "Ninguna sala proyecto esa pelicula");

        } else {

            for (Integer x : prod.obtenerIdSalaXPelicula(pelicula.getIdPelicula())) {
                
                Sala sala = sd.buscarSalaXId(x);

                modelo.addRow(new Object[]{sala.getUbicacion(), sala.getLocalidad(), sala.isEstado()});

            }
        }

    }

