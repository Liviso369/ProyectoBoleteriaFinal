    private void jbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarActionPerformed
        int fila = jtSala.getSelectedRow();

        if (fila != -1) {

            String ubicacion = (String) modelo.getValueAt(fila, 0);
            String localidad = (String) modelo.getValueAt(fila, 1);
            Boolean estado = Boolean.parseBoolean(modelo.getValueAt(fila, 2).toString());

            Pagina8 p8 = new Pagina8(ubicacion, localidad, estado);
            p8.setSize(1000, 530);
            p8.setLocation(0, 0);

            jpContenedor.removeAll();
            jpContenedor.add(p8, BorderLayout.CENTER);
            jpContenedor.revalidate();
            jpContenedor.repaint();
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro");
        }
    }//GEN-LAST:event_jbActualizarActionPerformed


