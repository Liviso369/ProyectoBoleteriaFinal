    private void jbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarActionPerformed
        if (jtfNombre.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Rellene todo los campos");
        } else {

            Pattern pat = Pattern.compile("^[^\\s][a-zA-ZÀ-ÿ0-9\\W]{1,30}$");
            Matcher mat = pat.matcher(jtfNombre.getText());

            if (mat.matches()) {

                String nombre1 = jtfNombre.getText();
                String genero1 = (String) jcbGenero.getSelectedItem();
                boolean estado1 = jcbEstado.isSelected();

                Pelicula pelicula1 = new Pelicula(id, nombre1, genero1, estado1);

                pd.actualizarPeliculas(pelicula1);
            }
        }
    }//GEN-LAST:event_jbActualizarActionPerformed


