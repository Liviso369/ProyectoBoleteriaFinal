    private void jtfNombreFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtfNombreFocusLost
        Pattern pat = Pattern.compile("^[^\\s][a-zA-ZÀ-ÿ0-9\\W]{1,30}$");
        Matcher mat = pat.matcher(jtfNombre.getText());
        if (!jtfNombre.getText().equals("") && !mat.matches()) {
            JOptionPane.showMessageDialog(this, "El nombre de la pelicula debe ir 1 a 30 cararacteres");
            jtfNombre.requestFocus();
        }
    }//GEN-LAST:event_jtfNombreFocusLost

    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        if (jtfNombre.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Rellene todo los campos");

        } else {

            Pattern pat = Pattern.compile("^[^\\s][a-zA-ZÀ-ÿ0-9\\W]{1,30}$");
            Matcher mat = pat.matcher(jtfNombre.getText());

            if (mat.matches()) {

                String nombre1 = jtfNombre.getText();
                String genero1 = (String) jcbGenero.getSelectedItem();
                boolean estado1 = jcbEstado.isSelected();

                Pelicula pelicula1 = new Pelicula(nombre1, genero1, estado1);

                pd.guardarPelicula(pelicula1);
            }
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

